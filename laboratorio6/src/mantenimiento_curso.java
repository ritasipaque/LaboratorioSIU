    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/m_c", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from cursos where codigodecurso = ?");
            pst.setString(1, codigo.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                nombre.setText(rs.getString("codigodecurso"));
                codigo.setText(rs.getString("nombredecurso"));
                 status.setText(rs.getString("statusdecurso"));
             
            } else {
                JOptionPane.showMessageDialog(null, "Alumno no registrado.");
            }
            
        }catch (Exception e){
            
        }
        


        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

